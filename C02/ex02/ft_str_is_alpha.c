int	ft_str_is_alpha(char *str)
{
	int	n;
	int	a;
	int	b;

	n = 0;
	while (str[n] != '\0')
	{
		a = (str[n] >= 65 && str[n] <= 90);
		b = (str[n] >= 97 && str[n] <= 122);
		if (!(a || b))
			return (0);
		n++;
	}
	return (1);
}
